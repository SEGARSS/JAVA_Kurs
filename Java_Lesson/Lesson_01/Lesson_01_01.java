/*Типы данных и пример объявления переменной.*/
public class Lesson_01_01 {
    public static void main(String[] args) {
        String s = "Proger Priva";
        System.out.println(s);
    }
}
/*В данном примере, мы обязанны не просто объявить переменную (s) но и сразу присвоить ей
значение, или в итоге у нас ничего не будет рабоать. Тобижь писать вот так - String s; - 
бесполезно, нужно чтото туда ложить чтобы мы получили результат и работу программы.
*/