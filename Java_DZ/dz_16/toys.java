package Java_DZ.dz_16;

public class toys {
    private int id;
    private String name;
    private int frequency;

    public toys(int id, String name, int frequency) {
        this.id = id;
        this.name = name;
        this.frequency = frequency;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getFrequency() {
        return frequency;
    }
}

